//! Provides functionality to parse and validate ELF binaries.

use crate::{Binary, Relocation, Segment, MAX_SEGMENTS};
use alloc::vec::Vec;
use memory::constants::USERSPACE;
use memory::{PageTableFlags, VirtAddr, VirtAddrRange, VirtPage, VirtPageSize};
use xmas_elf::header::{Class, Data, Machine, Version};
use xmas_elf::program::ProgramHeader;
use xmas_elf::sections::SectionData;
use xmas_elf::{header, program, sections, ElfFile};

/// Determines whether the given binary is likely
/// and ELF binary.
///
pub fn is_elf(name: &str, content: &[u8]) -> bool {
    // We don't care about the name for ELF binaries.
    _ = name;
    content.starts_with(&[0x7f, b'E', b'L', b'F'])
}

/// Parse an ELF binary, validating its structure.
///
/// If `parse` returns an `Ok` result, no further
/// checks will be necessary.
///
/// If `userspace` is `true`, then all program
/// segments and the entry point must exist purely
/// within userspace.
///
pub fn parse_elf<'bin>(binary: &'bin [u8], userspace: bool) -> Result<Binary, &'static str> {
    const GNU_STACK: program::Type = program::Type::OsSpecific(1685382481); // GNU stack segment.

    let elf = ElfFile::new(binary)?;
    header::sanity_check(&elf)?;

    match elf.header.pt1.class() {
        Class::SixtyFour => {}
        Class::ThirtyTwo => return Err("32-bit binaries are not supported"),
        _ => return Err("unknown binary class"),
    }

    match elf.header.pt1.data() {
        Data::LittleEndian => {}
        Data::BigEndian => return Err("big endian binaries are not supported"),
        _ => return Err("unknown binary data"),
    }

    match elf.header.pt1.version() {
        Version::Current => {}
        _ => return Err("unknown binary version"),
    }

    // We ignore the OS ABI.

    match elf.header.pt2.machine().as_machine() {
        Machine::X86_64 => {}
        _ => return Err("unsupported instruction set architecture"),
    }

    let entry_point = VirtAddr::try_new(elf.header.pt2.entry_point() as usize)
        .map_err(|_| "invalid entry point virtual address")?;

    let relocatable = match elf.header.pt2.type_().as_type() {
        header::Type::Executable => {
            if userspace && !USERSPACE.contains_addr(entry_point) {
                // We are strict for static executables.
                return Err("invalid entry point outside userspace");
            }

            false
        }
        header::Type::SharedObject => {
            // No need to check the entry point, as it's
            // relative and will be adjusted later.
            true
        }
        _ => return Err("unsupported binary type"),
    };

    // Collect the program segments, checking everything
    // is correct. We want to ensure that once we allocate
    // and switch to a new page table, we won't encounter
    // any errors and have to switch back. We also check
    // that none of the segments overlap.
    let mut segments: Vec<Segment> = Vec::new();
    let mut regions: Vec<VirtAddrRange> = Vec::new();
    for prog in elf.program_iter() {
        program::sanity_check(prog, &elf)?;
        match prog {
            ProgramHeader::Ph64(header) => {
                let typ = header.get_type()?;
                match typ {
                    program::Type::Load => {
                        if header.mem_size < header.file_size {
                            return Err("program segment is larger on disk than in memory");
                        }

                        if segments.len() >= MAX_SEGMENTS {
                            return Err("too many program segments");
                        }

                        // Check the segment doesn't overlap with
                        // any of the others and that the entire
                        // virtual memory space is valid.
                        let start = VirtAddr::try_new(header.virtual_addr as usize)
                            .map_err(|_| "invalid virtual address in program segment")?;
                        let end_addr = header
                            .virtual_addr
                            .checked_add(header.mem_size)
                            .ok_or("invalid memory size in program segment")?;
                        let end = VirtAddr::try_new(end_addr as usize)
                            .map_err(|_| "invalid memory size in program segment")?;
                        let range = VirtAddrRange::new(start, end);
                        for other in regions.iter() {
                            if range.overlaps_with(other) {
                                return Err("program segments overlap");
                            }
                        }

                        if !relocatable && userspace && !USERSPACE.contains(&range) {
                            return Err("program segment is outside userspace");
                        }

                        let data = header.raw_data(&elf);
                        let mut flags = PageTableFlags::PRESENT;
                        if userspace {
                            flags |= PageTableFlags::USER_ACCESSIBLE;
                        }
                        if !header.flags.is_execute() {
                            flags |= PageTableFlags::NO_EXECUTE;
                        }
                        if header.flags.is_write() {
                            flags |= PageTableFlags::WRITABLE;
                        }

                        if flags.contains(PageTableFlags::WRITABLE)
                            && !flags.contains(PageTableFlags::NO_EXECUTE)
                        {
                            return Err("program segments cannot be both writable and executable");
                        }

                        // Check that the segment flags are correct.
                        //
                        // We've already ensured that no two segments overlap in
                        // memory. This checks that where two (or more) segments
                        // share a page, they all agree on the page flags. We have
                        // few enough segments that we need not worry about this
                        // check being particularly efficient.
                        let start_page =
                            VirtPage::containing_address(start, VirtPageSize::Size4KiB);
                        let end_page = VirtPage::containing_address(end, VirtPageSize::Size4KiB);
                        for other in segments.iter() {
                            if (start_page.contains(other.start)
                                || start_page.contains(other.end)
                                || end_page.contains(other.start)
                                || end_page.contains(other.end))
                                && other.flags != flags
                            {
                                return Err("program segments with different flags share a page");
                            }
                        }

                        let align = header.align as usize;
                        let segment = Segment {
                            start,
                            end,
                            data,
                            flags,
                            align,
                        };

                        regions.push(range);
                        segments.push(segment);
                    }
                    program::Type::Tls => {
                        return Err("thread-local storage is not yet supported");
                    }
                    program::Type::Interp => {
                        return Err("interpreted binaries are not yet supported");
                    }
                    GNU_STACK => {
                        if header.flags.is_execute() {
                            return Err("executable stacks are not supported");
                        }
                    }
                    _ => {} // Ignore for now.
                }
            }
            ProgramHeader::Ph32(_) => return Err("32-bit binaries are not supported"),
        }
    }

    // Now that we've parsed all the segments,
    // we parse any relocation tables. We do
    // these separately so we can bounds-check
    // the relocations more easily.
    const R_X86_64_RELATIVE: u32 = 8;
    let mut relocations: Vec<Relocation> = Vec::new();
    for section in elf.section_iter() {
        sections::sanity_check(section, &elf)?;
        match section.get_data(&elf)? {
            SectionData::Rel64(table) => {
                // These relocations increment the data in situ.
                for relocation in table {
                    let idx = relocation.get_symbol_table_index();
                    if idx != 0 {
                        return Err("relocations using the symbol table are not supported");
                    }

                    match relocation.get_type() {
                        R_X86_64_RELATIVE => {
                            // Check the relocation exists in one
                            // of the segments.
                            let addr = VirtAddr::try_new(relocation.get_offset() as usize)
                                .map_err(|_| "relocation for invalid address")?;
                            regions
                                .iter()
                                .find(|region| region.contains_addr(addr))
                                .ok_or("relocation for address not in binary")?;
                            let base = None;

                            let relocation = Relocation { addr, base };
                            relocations.push(relocation);
                        }
                        _ => return Err("unsupported relocation type"),
                    }
                }
            }
            SectionData::Rela64(table) => {
                // These relocations include the base value in the relocation.
                for relocation in table {
                    let idx = relocation.get_symbol_table_index();
                    if idx != 0 {
                        return Err("relocations using the symbol table are not supported");
                    }

                    match relocation.get_type() {
                        R_X86_64_RELATIVE => {
                            // Check the relocation exists in one
                            // of the segments.
                            let addr = VirtAddr::try_new(relocation.get_offset() as usize)
                                .map_err(|_| "relocation for invalid address")?;
                            regions
                                .iter()
                                .find(|region| region.contains_addr(addr))
                                .ok_or("relocation for address not in binary")?;
                            let base = Some(relocation.get_addend());

                            let relocation = Relocation { addr, base };
                            relocations.push(relocation);
                        }
                        _ => return Err("unsupported relocation type"),
                    }
                }
            }
            _ => {}
        }
    }

    // Check that the entry point is in one of the
    // segments.
    regions
        .iter()
        .find(|&region| region.contains_addr(entry_point))
        .ok_or("entry point is not in any program segment")?;

    Ok(Binary {
        entry_point,
        segments,
        relocatable,
        relocations,
    })
}

