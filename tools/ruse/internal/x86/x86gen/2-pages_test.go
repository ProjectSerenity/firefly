func TestReadInstructionPages(t *testing.T) {
	type TestPage struct {
		Page  int
		Next  int
		Name  string
		Pages []Page
	}

	tests := []struct {
		Name  string
		File  string
		Pages []TestPage
	}{
		{
			Name: "Vol2a",
			File: "Volume-2a-Instruction-Set-Reference-A-L",
			Pages: []TestPage{
				{
					// No instruction, take us to the next one.
					Page: 180,
					Next: 181,
				},
				{
					// And again, but skip over multiple pages.
					Page: 126,
					Next: 129,
				},
				{
					// Single page.
					Page:  190,
					Next:  191,
					Name:  "ANDN-Logical AND NOT",
					Pages: []Page{page190},
				},
				{
					// Double page.
					Page:  206,
					Next:  208,
					Name:  "BLENDPD-Blend Packed Double Precision Floating-Point Values",
					Pages: []Page{page206, page207},
				},
				{
					// Multi-page instruction.
					Page:  657,
					Next:  662,
					Name:  "Jcc-Jump if Condition Is Met",
					Pages: []Page{page657, page658, page659, page660, page661},
				},
			},
		},
	}

	for _, test := range tests {
		t.Run(test.Name, func(t *testing.T) {
			name := filepath.Join("testdata", test.File+".pdf")
			r, err := openPDF(name)
			if err != nil {
				t.Fatalf("failed to open %q: %v", test.Name, err)
			}

			for _, page := range test.Pages {
				t.Run(strconv.Itoa(page.Page), func(t *testing.T) {
					name, text, next := ReadInstructionPages(r, page.Page, false)
					if name != page.Name {
						t.Fatalf("%s p.%d: got name %q, want %q", test.Name, page.Page, name, page.Name)
					}

					if next != page.Next {
						t.Fatalf("%s p.%d: got next page %d, want %d", test.Name, page.Page, next, page.Next)
					}

					if diff := cmp.Diff(page.Pages, text); diff != "" {
						// Get the debug output.
						ReadInstructionPages(r, page.Page, true)
						t.Fatalf("%s p.%d: (-want, +got)\n%s", test.Name, page.Page, diff)
					}
				})
			}
		})
	}
}

